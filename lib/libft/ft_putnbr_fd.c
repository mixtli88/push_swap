#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t	i;
	long	nb;

	nb = n;
	i = 0;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	i = (nb % 10) + '0';
	write(fd, &i, 1);
}
